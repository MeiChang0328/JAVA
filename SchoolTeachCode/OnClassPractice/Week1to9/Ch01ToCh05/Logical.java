package Week1to9.Ch01ToCh05;

public class Logical {
    public static void main(String[] argv) {
        boolean a = true, b = false;
        System.out.println("a= " + a + ", b= " + b);
        System.out.println("a &  b：" + (a & b) );
        System.out.println("a && b：" + (a && b));
        System.out.println("a |  b：" + (a | b) );
        System.out.println("a || b：" + (a || b));
        System.out.println("a ^  b：" + (a ^ b) );
    }
}
